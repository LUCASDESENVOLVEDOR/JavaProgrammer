package br.com.impacta.booleanos;

public class VariaveisBoolean {
	
	public static void main(String[] args) {
		
		boolean b1 = true;
		int numero = 10;
				
		// = atribuição		
		// == comparação
		
		boolean b2 = (numero == 10); //true	
		
		System.out.println("B1: " + b1);
		System.out.println("B2: " + b2);	
		
		
	}

}
















